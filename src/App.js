import { useEffect, useState } from "react";
import StarRating from "./StarRating";

// TODO add sorting

const KEY = "a6db8c6a";

const average = (arr) =>
  arr.reduce((acc, cur, i, arr) => acc + cur / arr.length, 0);

//////////////////////////////////////////////////////////////////////////////////////////
export default function App() {
  const [query, setQuery] = useState("");
  const [movies, setMovies] = useState([]);
  const [isLoading, setIsLoading] = useState(false);
  const [error, setError] = useState("");
  const [selectedId, setSelectedId] = useState(null);

  // const [watched, setWatched] = useState([]);
  // no argument / pure function
  // just at mount(initial render)
  const [watched, setWatched] = useState(() =>
    //WOW
    JSON.parse(localStorage.getItem("watched"))
  );
  // useState( JSON.parse(localStorage.getItem("watched"))) this will call this fn evert render
  // NONO cuz we are calling a funtion but not passing the function in

  const handleSelectMovie = (id) => {
    setSelectedId((selectedId) => (selectedId === id ? null : id));
  };

  const handleCloseMovie = () => {
    setSelectedId(null);
  };

  const handleAddWatched = (movie) => {
    setWatched((watched) => [...watched, movie]);
    handleCloseMovie();
    // 1) WOW but not very good
    // localStorage.setItem("watched", JSON.stringify([...watched, movie])); // async
  };

  const handleDeleteWatched = (id) => {
    setWatched((watched) => watched.filter((movie) => movie.imdbID !== id));
  };

  useEffect(() => {
    localStorage.setItem("watched", JSON.stringify(watched)); //WOW
  }, [watched]);

  useEffect(
    function () {
      // const controller = new AbortController();

      async function fetchMovie() {
        try {
          setIsLoading(true);
          setError("");

          const res = await fetch(
            `http://www.omdbapi.com/?apikey=${KEY}&s=${query}`
            // { signal: controller.signal }
          );
          if (!res.ok)
            throw new Error("Something went wrong with fetch movies ü§™");

          const data = await res.json();
          if (data.Response === "False") throw new Error("Movie not found ü•≤");

          setMovies(data.Search);
          // setError("");
        } catch (err) {
          console.error(err.message);
          if (err.name !== "AbortError") setError(err.message);
        } finally {
          setIsLoading(false);
        }
      }

      if (query.length < 3) {
        setError("");
        setMovies([]);
        return;
      }

      // fetchMovie();

      // return function () {
      //   controller.abort();
      // };

      // Deboucing WOW
      handleCloseMovie(); //BUG
      const timer = setTimeout(fetchMovie, 800);
      // Clean up function
      return () => clearTimeout(timer);
    },
    [query]
  );

  return (
    <>
      <NavBar>
        <Logo />
        <Search query={query} setQuery={setQuery} />
        <NumResult movies={movies} />
      </NavBar>
      <Main>
        {/* <Box element={<MovieList movies={movies} />} /> SAME*/}
        <Box>
          {isLoading ? (
            <Loader />
          ) : error ? (
            <ErrorMessage message={error} />
          ) : (
            <MovieList movies={movies} OnSelectMovie={handleSelectMovie} />
          )}
        </Box>
        <Box>
          {selectedId ? (
            <MovieDetails
              selectedId={selectedId}
              onCloseMovie={handleCloseMovie}
              onAddWatched={handleAddWatched}
              watched={watched}
            />
          ) : (
            <>
              <Summary watched={watched} />
              <WatchedMoviesList
                watched={watched}
                onDeleteWatched={handleDeleteWatched}
              />
            </>
          )}
        </Box>
      </Main>
    </>
  );
}

const NavBar = ({ children }) => {
  return <nav className="nav-bar">{children}</nav>;
};

const Logo = () => {
  return (
    <div className="logo">
      <span role="img">üçø</span>
      <h1>usePopcorn</h1>
    </div>
  );
};

